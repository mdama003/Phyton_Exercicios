# Float - Número de ponto flutuante com casas decimais

num3 = 12.8
# outra forma de declarar um número como float
num4 = float(19.0)
# Ao declarar um número decimal como inteiro o compilador entende que o número não tem casas decimais
num5 = int(16.5)

print (num3)
print (num5)
# mostra a classe que o número pertence, neste caso float
print (type(num4))